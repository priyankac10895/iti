                     <h2>Why You Choose ITI Fitter?</h2>   
                    <p>There are Millions of students who wants’ job in technical field as a technician in Electrical and as a junior  engineer in Fitter field after their schooling, so those students who want to join as a technician or junior engineer in Fitter branch in industry, he/she Can do ITI Fitter Course for 2 year program, who did his/her  10th .</p>
                    <p><strong>And ITI give chance to enter into the Engineering Diploma college in the second year.</strong></p>
                   <h3>Future of ITI Fitter:-</h3>
                   <p><strong>An ITI (Fitter)</strong></p>
                   <p>Teaches a student all the fundamental concepts of Technology / Mechanism /Engineering for those  students who want to become a Fitter/ fabricator / Engineers in Fabrication Field. An ITI (Fitter) is an  extremely professional course where students can choose their specializations and pursue their career  interest.</p>
<p>Once the students earn their ITI, they can take up a job in the engineering field every where. Another  advantage of a ITI(Fitter) is that students can a get chance to enter into the Diploma college in the second year.</p>
<p>In the second year of Diploma College a few percentage of seats are reserved for ITI students depending on  the ITI that a student has selected. Hence this ITI is also referred to as ‘backdoor to Diploma engineering’. Thus all engineering aspirants can take up a ITI course in this field and become the future engineers of the  country.</p>
					<h3>COURSE INFORMATION :- (TRADE: Fitter)</h3>

					<ol>
                    	<li>
                        	<h4>INTRODUCTION:</h4>
                            <p>This course is meant for the candidates who aspire to become a professional fitter.</p>
                        </li>
                        <li>
                        	<h4>Terminal Competency/Deliverables:</h4>
                            <p>After successful completion of this course the trainee shall be able to perform the following skills with proper sequence.</p>
                            <ul>
                            <li>The trainees can work in the industry as semi-skilled fitter.</li>
                            <li>The trainee can work in the field of pipe fitting, lathe, drilling, welding, Inspection &amp; measurement, <br />
                            general fitting work observing safety precautions.</li>
                            <li>The trainees can work on Dismantle &amp; assemble of various valves, test the accuracy of Machine tools.</li>
                            <li>Perform simple repair on machinery, dovetail slides and assemble with location dowel pins, stud and bolts.</li>
                            <li>Prepare snap gauge for checking diameters to an accuracy of ± 0.02 mm</li>
                            <li>Handle different type of Fire extinguishers</li>
                            </ul>
                        </li>
                        <li>
                        	<h4>Employment opportunities:</h4>
                            <p><strong>On successful completion of this course, the candidates shall be gainfully employed in the following industries:</strong></p>
                            <ul>
                            <li>Production &amp; Manufacturing industries.</li>
                            <li>Structural Fabrication like bridges, Roof structures, Building &amp; construction.</li>
                            <li>Automobile and allied industries</li>
                            <li>Service industries like road transportation and Railways.</li>
                            <li>Ship building and repair</li>
                            <li>Infrastructure and defence organisations</li>
                            <li>In public sector industries like BHEL, BEML, NTPC, etc and private industries in India &amp; abroad.</li>
                            <li>Self employment</li>
                            </ul>
                        </li>
                        <li>
                        	<h4>Further learning pathways:</h4>
                            <ul>
                            <li>On successful completion of the course trainees can pursue Apprenticeship training in the reputed Industries / Organizations.</li>
                            <li>On successful completion of the course trainees can opt for Diploma course (Lateral entry).</li>
                            <li>On successful completion of the course trainees can opt for CITS course.</li>
                            </ul>
                        </li>                         
                    </ol> 
					 
